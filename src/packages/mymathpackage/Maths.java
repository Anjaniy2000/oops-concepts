package packages.mymathpackage;

public class Maths {

    public static final double PI = 3.141592653589793;

    public static int getMin(int a, int b){
        if(a < b) return a;
        else return b;
    }

    public static int getMax(int a, int b){
        if(a > b) return a;
        else return b;
    }
}
